func TestSQLBackendInterfaceDefaultsToMemoryFallback(t *testing.T) {
	server := NewServer(Config{Database: "dev", User: "dev"})

	result, err := server.backend.Exec(context.Background(), "select 1")
	if err != nil {
		t.Fatalf("Exec(select 1) error = %v", err)
	}
	if result.Tag != "SELECT 1" || len(result.Rows) != 1 || result.Rows[0][0] != "1" {
		t.Fatalf("result = %#v", result)
	}
}

func TestMemoryBackendCatalogReflectsRedshiftMetadata(t *testing.T) {
	server := NewServer(Config{Database: "dev", User: "dev"})
	if _, err := server.executeSQL("create table events(id int, payload varchar(64)) diststyle key distkey(id) sortkey(id)"); err != nil {
		t.Fatalf("create table: %v", err)
	}

	catalog, err := server.backend.Catalog(context.Background())
	if err != nil {
		t.Fatalf("Catalog() error = %v", err)
	}
	table := findBackendTable(catalog, "public", "events")
	if table == nil {
		t.Fatalf("catalog missing public.events: %#v", catalog)
	}
	if len(table.Columns) != 2 || table.Columns[0].Name != "id" || table.Columns[1].Name != "payload" {
		t.Fatalf("columns = %#v", table.Columns)
	}
}

func TestMemoryBackendRejectsExecAfterClose(t *testing.T) {
	backend := memory.New(func(ctx context.Context, statement string) (backend.Result, error) {
		return backend.Result{Tag: "SELECT 1"}, nil
	}, nil)
	if err := backend.Close(); err != nil {
		t.Fatalf("Close() error = %v", err)
	}

	if _, err := backend.Exec(context.Background(), "select 1"); err == nil {
		t.Fatal("Exec after Close() error = nil")
	}
}

func TestSQLBackendCanBeInjected(t *testing.T) {
	expectedErr := errors.New("backend unavailable")
	server := NewServer(Config{SQLBackend: failingBackend{err: expectedErr}})

	if _, err := server.executeSQL("select 1"); !errors.Is(err, expectedErr) {
		t.Fatalf("executeSQL error = %v, want %v", err, expectedErr)
	}
}

