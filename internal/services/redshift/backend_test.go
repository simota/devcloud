func TestRedshiftTranslatorExtractsTableAttributesForPostgresBackend(t *testing.T) {
	backend := &recordingBackend{result: backend.Result{Tag: "CREATE TABLE"}}
	server := NewServer(Config{
		SQLBackend: backend,
		Translator: translator.NewRedshiftToPostgres(),
	})

	_, err := server.executeSQL(`create table analytics.events(
		id integer identity(1,1) distkey sortkey encode raw,
		payload varchar(64) default 'unknown'
	) diststyle key distkey(id) sortkey(id) backup no`)
	if err != nil {
		t.Fatalf("executeSQL(create table) error = %v", err)
	}
	if strings.Contains(strings.ToLower(backend.statement), "diststyle") ||
		strings.Contains(strings.ToLower(backend.statement), "distkey") ||
		strings.Contains(strings.ToLower(backend.statement), "sortkey") ||
		strings.Contains(strings.ToLower(backend.statement), "encode") ||
		strings.Contains(strings.ToLower(backend.statement), "backup") {
		t.Fatalf("backend SQL still contains Redshift-only attributes: %s", backend.statement)
	}
	if !strings.Contains(strings.ToLower(backend.statement), "generated by default as identity") {
		t.Fatalf("backend SQL did not rewrite identity: %s", backend.statement)
	}

	catalog := server.CatalogSnapshot()
	if len(catalog.Tables) != 1 || catalog.Tables[0].Schema != "analytics" || catalog.Tables[0].DistStyle != "key" || catalog.Tables[0].DistKey != "id" {
		t.Fatalf("table metadata = %#v", catalog.Tables)
	}
	if len(catalog.Tables[0].SortKeys) != 1 || catalog.Tables[0].SortKeys[0] != "id" {
		t.Fatalf("sort keys = %#v", catalog.Tables[0].SortKeys)
	}
	if !columnSnapshotHas(catalog.Columns, "id", "raw", "", true) {
		t.Fatalf("id column metadata = %#v", catalog.Columns)
	}
	if !columnSnapshotHas(catalog.Columns, "payload", "", "'unknown'", false) {
		t.Fatalf("payload column metadata = %#v", catalog.Columns)
	}
}

func findBackendTable(catalog backend.CatalogSnapshot, schemaName string, tableName string) *backend.Table {
	for _, schema := range catalog.Schemas {
		if schema.Name != schemaName {
			continue
		}
		for i := range schema.Tables {
			if schema.Tables[i].Name == tableName {
				return &schema.Tables[i]
			}
		}
	}
	return nil
}

type failingBackend struct {
	err error
}

func (b failingBackend) Exec(ctx context.Context, statement string) (backend.Result, error) {
	return backend.Result{}, b.err
}

func (b failingBackend) Begin(ctx context.Context) (backend.Transaction, error) {
	return nil, b.err
}

func (b failingBackend) Catalog(ctx context.Context) (backend.CatalogSnapshot, error) {
	return backend.CatalogSnapshot{}, b.err
}

func (b failingBackend) Close() error {
	return nil
}

type recordingBackend struct {
	statement string
	result    backend.Result
}

func (b *recordingBackend) Exec(ctx context.Context, statement string) (backend.Result, error) {
	b.statement = statement
	return b.result, ctx.Err()
}

func (b *recordingBackend) Begin(ctx context.Context) (backend.Transaction, error) {
	return nil, errors.New("transaction is not implemented in recording backend")
}

func (b *recordingBackend) Catalog(ctx context.Context) (backend.CatalogSnapshot, error) {
	return backend.CatalogSnapshot{}, ctx.Err()
}

func (b *recordingBackend) Close() error {
	return nil
}

type rewriteTranslator struct {
	backendSQL   string
	sideEffects  []translator.SideEffect
	metadata     []translator.MetadataEffect
	parameters   []translator.Parameter
	handledByDev bool
}

func (t rewriteTranslator) Translate(ctx context.Context, session translator.Session, sql string) (translator.TranslationResult, error) {
	if err := ctx.Err(); err != nil {
		return translator.TranslationResult{}, err
	}
	return translator.TranslationResult{
		BackendSQL:        t.backendSQL,
		Parameters:        t.parameters,
		MetadataEffects:   t.metadata,
		SideEffects:       t.sideEffects,
		HandledByDevcloud: t.handledByDev,
	}, nil
}
